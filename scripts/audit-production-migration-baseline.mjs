#!/usr/bin/env node
/**
 * Offline-only reconciliation aid. It imports a schema-only SQLite export into
 * a temporary database and compares migration-owned structural requirements to
 * the repository's official replay. It intentionally has no remote client.
 */
import { existsSync, mkdtempSync, readFileSync, readdirSync, rmSync, writeFileSync, mkdirSync } from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { importSchemaSql, introspectSqliteDb, replayMigrations } from './db-tool.mjs';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const migrationDir = path.join(root, 'migrations');
const auditedRange = /^00(?:0[7-9]|[12]\d|3[0-6])_.*\.sql$/;
const dataMutation = /\b(?:INSERT|UPDATE|DELETE|REPLACE)\b/i;

function listMigrations() {
  return readdirSync(migrationDir).filter(file => auditedRange.test(file)).sort();
}

function requirementsFor(file, sql, expected) {
  const requirements = [];
  for (const match of sql.matchAll(/ALTER\s+TABLE\s+([\w]+)\s+ADD\s+COLUMN\s+([\w]+)/gi)) requirements.push({ kind: 'column', table: match[1], name: match[2] });
  for (const match of sql.matchAll(/CREATE\s+(?:UNIQUE\s+)?INDEX\s+IF\s+NOT\s+EXISTS\s+([\w]+)/gi)) requirements.push({ kind: 'index', name: match[1] });
  for (const match of sql.matchAll(/CREATE\s+TABLE\s+IF\s+NOT\s+EXISTS\s+([\w]+)/gi)) requirements.push({ kind: 'table', name: match[1] });
  for (const match of sql.matchAll(/DROP\s+INDEX\s+IF\s+EXISTS\s+([\w]+)/gi)) requirements.push({ kind: 'absent-index', name: match[1] });
  // A requirement must be known to the official replay, except an intentionally
  // dropped index, which is expected to be absent from that replay.
  return requirements.filter(requirement => requirement.kind === 'absent-index' || expected.objects.some(object => object.name === requirement.name));
}

function requirementSatisfied(requirement, actual) {
  if (requirement.kind === 'column') return actual.columns[requirement.table]?.some(column => column.name === requirement.name) ?? false;
  if (requirement.kind === 'absent-index') return !actual.objects.some(object => object.type === 'index' && object.name === requirement.name);
  return actual.objects.some(object => object.name === requirement.name);
}

export function classifyMigration({ file, sql, actual, expected }) {
  const requirements = requirementsFor(file, sql, expected);
  const satisfied = requirements.filter(requirement => requirementSatisfied(requirement, actual));
  const missing = requirements.filter(requirement => !requirementSatisfied(requirement, actual));
  if (dataMutation.test(sql)) return { file, classification: 'DATA_CHECK_REQUIRED', requirements, satisfied, missing, reason: 'Migration contains data-changing SQL; structural evidence alone cannot prove its data effect.' };
  const classification = missing.length === 0 ? 'SATISFIED' : satisfied.length ? 'PARTIAL' : 'MISSING';
  return { file, classification, requirements, satisfied, missing, reason: null };
}

function reconciliationSql(results) {
  const safe = results.filter(result => result.classification === 'SATISFIED');
  return [
    '-- Generated by scripts/audit-production-migration-baseline.mjs. Review before running manually.',
    '-- This file intentionally includes only structurally SATISFIED migrations and performs no schema or data changes.',
    ...safe.map(result => `INSERT OR IGNORE INTO d1_migrations (name) VALUES ('${result.file}');`),
    ''
  ].join('\n');
}
function dataChecksSql(results) {
  const checks = results.filter(result => result.classification === 'DATA_CHECK_REQUIRED');
  return ['-- Generated offline. Review each migration data effect before reconciling its history.', ...checks.map(result => `-- DATA_CHECK_REQUIRED: ${result.file}`), ''].join('\n');
}
function markdown(report) {
  const byClass = name => report.results.filter(result => result.classification === name).map(result => `- ${result.file}`).join('\n') || '- nenhum';
  return `# Production D1 migration baseline audit\n\nSchema source: \`${report.schemaPath}\`\n\n## SATISFIED\n${byClass('SATISFIED')}\n\n## DATA_CHECK_REQUIRED\n${byClass('DATA_CHECK_REQUIRED')}\n\n## PARTIAL\n${byClass('PARTIAL')}\n\n## MISSING\n${byClass('MISSING')}\n`;
}

export function auditProductionMigrationBaseline({ schemaPath, outputDir = path.join(root, 'release') }) {
  if (!schemaPath || !existsSync(schemaPath)) throw new Error('A readable schema-only SQLite export path is required.');
  const temp = mkdtempSync(path.join(os.tmpdir(), 'portal-lm-baseline-audit-'));
  let replay;
  try {
    const actualDb = path.join(temp, 'production-schema.sqlite');
    // `.schema` can include SQLite's implementation detail for AUTOINCREMENT;
    // SQLite recreates it as needed and rejects importing it explicitly.
    const schemaSql = readFileSync(schemaPath, 'utf8').replace(/^CREATE TABLE sqlite_sequence\([^;]*\);\s*$/gmi, '');
    importSchemaSql({ schemaSql, databasePath: actualDb });
    replay = replayMigrations();
    if (!replay.ok) throw new Error(`Official migration replay failed: ${replay.error?.migration ?? replay.error?.message ?? 'unknown error'}`);
    const actual = introspectSqliteDb(actualDb);
    const expected = introspectSqliteDb(replay.database);
    const results = listMigrations().map(file => classifyMigration({ file, sql: readFileSync(path.join(migrationDir, file), 'utf8'), actual, expected }));
    const report = { schemaPath: path.resolve(schemaPath), auditedRange: '0007-0036', offlineOnly: true, results };
    mkdirSync(outputDir, { recursive: true });
    writeFileSync(path.join(outputDir, 'migration-baseline-audit.json'), `${JSON.stringify(report, null, 2)}\n`);
    writeFileSync(path.join(outputDir, 'migration-baseline-audit.md'), markdown(report));
    writeFileSync(path.join(outputDir, 'production-migration-data-checks.sql'), dataChecksSql(results));
    writeFileSync(path.join(outputDir, 'reconcile-d1-migration-history.sql'), reconciliationSql(results));
    return report;
  } finally {
    if (replay?.database) rmSync(path.dirname(replay.database), { recursive: true, force: true });
    rmSync(temp, { recursive: true, force: true });
  }
}

const isMain =
  Boolean(process.argv[1]) &&
  path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isMain) {
  try {
    const report = auditProductionMigrationBaseline({
      schemaPath: process.argv[2],
    });

    process.stdout.write(
      `${JSON.stringify(
        {
          generated: true,
          results: report.results.map(({ file, classification }) => ({
            file,
            classification,
          })),
        },
        null,
        2
      )}\n`
    );
  } catch (error) {
    process.stderr.write(`${error.message}\n`);
    process.exitCode = 1;
  }
}
